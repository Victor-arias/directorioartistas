	</p>

	<h4>JOSE MIGUEL JUVINAO - MUSICA</h4>
	<p>
	Maestro en Contrabajo, Universidad de Antioquia, Medellín-Colombia.
	Maestría en Modern American Music, Longy School of Music of Bard College, Cambridge-Massachusetts. Alumno de bajo de Ilko Russev, John Lockwood y Pascale Delache-Feldman y otros grandes bajistas en la escena mundial .
	En el campo orquestal ha pertenecido a  la Orquesta Filarmónica de Medellín (1999).
	Sinfónica Juvenil de Antioquia (1999-2002), Banda estudiantes Universidad de Antioquia (2007-2009), Longy Conservatory Orchestra LCO (2011-2013), Boston Civic Orchestra (2011).
	Ha participado como músico de sesión en 75 trabajos discográficos.
	En pasadas versiones del Jam Latino se desempeñó como jefe demontaje y director en tarima.
	Recientemente  volvió a Colombia luego de su temporada de estudios en el Berklee College of Music en Boston, USA.
	</p>


	<h4>MARIA CLAUDIA MEJIA - DANZA</h4>
	<p>
	Máster en Prácticas Artísticas Contemporáneas y Diseminación (Máster in Contemporary Arts Practice and Dissemination) – Énfasis: Coreografía. Universidad de Girona – L´Animal a L´Esquena. Girona, España. 2010 
	Comunicadora social – Periodista Universidad de Antioquia, Colombia. 1997 
	Diplomada en Danza Contemporánea, Escuela Popular de Arte. 2000 
	Artista invitada a la Exposición Antioquias – Museo de Antioquia 2013, videodanza Fría Mañana 
	Becas a la Creación del Ayuntamiento de Girona, España 2009 en danza, obra Intercambio de Piel. 
	Beca de Creación Alcaldía de Medellín 2007 con la obra Besos en El Parque, danza en espacios no convencionales. 
	Beca de Residencia Artística Colombia- Reino Unido 2006 Independent Dance/Visiting Arts 
	Premio Danza en Espacios No Convencionales, obra Habitación con Vista, Festival Distrital de Danza Contemporánea de Bogotá, 2005, con la Cía. La Prenda. 
	Premio Distrital a Bailarines de Danza Contemporánea IDCT, Bogotá 2004, bailarina de la obra Juegos de Amor, director Newton Moraes (Brasil). 
	</p>


	<h4>CLARA CRISTINA ACOSTA - TEATRO</h4>
	<P>
	Maestra en Arte Dramático y Profesional en Filosofía y Letras con experiencia laboral en educación.
	Formación
	Maestría en Literatura, Universidad de Antioquia (actualmente)
	Profesional en Filosofía y Letras, Universidad Pontiﬁcia Bolivariana, 2010, Medellín-Colombia y UNAM-México. (Intercambio académico)
	Diploma Superior en Gestión Cultural, 17 Instituto de Estudios Críticos. 2007, México DF
	Maestra en Arte Dramático, Universidad de Antioquia, 2003 Medellín- Colombia
	2013-2014      Universidad de Antioquia. Coordinadora de Cine. Vicerrectoría de Extensión, Departamento de Extensión Cultural. (Actualmente)
	2013    One World Project.Programadora One World Film Series for Children. Brooklyn, EEUU Coordinación de películas en español para el Festival de Cine Infantil One World Series.
	2012    IV Parada Juvenil de Lectura.
	Coordinadora de Contenidos. Plan Municipal de Lectura
	Secretaría de Cultura, Medellín.
	Facultad de Artes y Humanidades, Instituto Tecnológico Metropolitano.
	Coordinadora Académica del Diplomado en Creación Audiovisual para jóvenes afro descendientes e indígenas.
	2011-2010      Centro de Extensión, Facultad de Artes, Universidad de Antioquia.
	</p>

	<h4>DIEGO LONDOÑO - PERIODISTA CULTURAL</h4>
	<p>
		Periodista de la Universidad de Antioquia, con énfasis en música, cultura y comunicación digital y estratégica.
		Músico. Realizador radial. Bloguero del periódico El Colombiano. Escribió los libros Medellín en Canciones 
		“El rock como cronista de la ciudad” y el libro Los Yetis “Una bomba atómica a go-go” la historia de los abuelos de
		nuestro rock. Productor de contenidos de Señal Radiónica, de la casa de medios públicos Señal Colombia.
		Columnista de la Revista Música de Colombia, coordinador de música de “Aquí Suena Medellín” proyecto de la Secretaría
		de Cultura y la USM. Director y fundador de Música Somos, periodismo musical y digital. Ha sido expositor 
		en Campus Party, Barcamp, Medellín se toma la palabra, también, jurado en los Premios de Periodismo Comunitario 
		y jurado musical en diferentes procesos como Altavoz, Batalla de las bandas Videmia, Nuestro bar y Sk8rules.
		Ha trabajado como jefe de prensa de Fito Páez, Andrés Calamaro, Jarabe de Palo, Sociedad FB7, Alejo García, EstebanGira y Unos/Vagabundos. Además ha entrevistado a muchos artistas locales e internacionales.
		Hizo parte de proyectos como Cartel Urbano, HagalaU y Altaír.

	</p>


