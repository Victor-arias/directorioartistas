			<p>
				<stong>La Alcaldía de Medellín, a través de la Secretaría de Cultura Ciudadana, invita a los artistas,
				entidades y agrupaciones locales a inscribir sus propuestas en el proceso de selección para la 
				programación de la Feria de las Flores 2012(?)</strong>
			</p>

			<p>
				Entre los participantes se elegirán las mejores propuestas que desde la <strong>música, el teatro, 
				la danza, el humor, la cuentería, la magia y otras disciplinas artísticas</strong>, harán parte
				de la programación oficial del evento.
			</p>

			<p>
				Los interesados deben inscribirse en el Directorio Artístico de Medellín, diligenciando un 
				<strong>Formulario en Línea</strong> al que podrán acceder desde el ícono que se encuentra al final
				de ésta página.
			</p>

			<h4>Condiciones de Participación</h4>

			<p>
				Para el proceso de inscripción y selección de los grupos y artistas, es importante conocer la siguiente
				información:
				<ul>
					<li>
						Esta inscripción es <strong>exclusiva</strong> para agrupaciones, artistas y organizadores
						culturales que tengan sede o desarrollen sus actividades en uno de los municipios del
						<strong>Área Metropolitana del Valle de Aburrá (Caldas, La Estrella, Sabaneta, Envigado, Itagüi,
						Medellín, Bello, Copacabana, Girardota y Barbosa).</strong> Propuestas de otras ciudades o
						paises no serán tenidas en cuenta.
					</li>
					<li>
						Las inscripciones se abrirán el <strong>8 de Junio y se cerrarán el 30 de Junio de 2012(?) a las
						6:00 p.m.</strong> hora legal colombiana (UTC -5). Solo se recibirán propuestas por éste medio.
					</li>
					<li>
						Las propuestas inscritas serán evaluadas por un jurado, conformado por cinco personas de 
						comprobada trayectoria e idoneidad en las artes y la organización de espectáculos, quienes
						seleccionarán las que mejor cumplan con los criterios de <strong>calidad interpretativa, 
						creatividad, trayectoria y viabilidad técnica y económica.</strong> En total, serán elegidas
						<strong>80 propuestas</strong>.
					</li>
					<li>
						El resultado con las 80 propuestas escogidas para la programación, se publicará el día 
						<strong>10 de Julio del 2012(?)</strong>, en la página web de la Alcaldía de Medellín y en el 
						portal Medellín Cultura.
					</li>
					<li>
						En todo momento, usted podrá saber cuáles y cuántas propuestas están inscritas, consultando el 
						catálogo en línea del Directorio Artístico de Medellín, al que podrá acceder desde el ícono que
						encontrará al final de ésta página.
					</li>
					<li>
						Antes de diligenciar el formulario es muy importante que <strong>lea detalladamente las 
						instrucciones</strong>, las podrá consultar desde el botón ubicado en la parte inferior de ésta
						página. También podrá acceder a estas desde el ícono ubicado en la parte superior del 
						formulario de inscripción.
					</li>
					<li>
						La organización de la Feria de las Flores 2012(?) aclara que <strong>diligenciar la inscripción
						no implica que la propuesta haya sido aceptada para participar en la programación</strong>. 
						Todas deben pasar por el proceso de selección del jurado.
					</li>
					<li>
						Al realizar la inscripción, los participantes dan a entender que aceptan y comprenden las 
						condiciones de este proceso de selección.
					</li>
					<li>
						Del <strong>lunes 25 de junio al viernes 29 de junio, entre las 8:00 a.m. y las 12:00 m.</strong>
						estará habilitado el servicio de <strong>Asistencia en Línea</strong> desde la página del 
						formulario, para aclarar cualquier inquietud que pueda presentarse con el trámite de las 
						inscripciones.
					</li>
					<li>
						En caso de dudas o inquietudes también puede escribir al correo 
