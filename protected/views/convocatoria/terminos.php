		<h4>Escenarios de programación de la Feria de las Flores 2013</h4>

		<p>
			Las agrupaciones seleccionadas serán programadas en los siguientes escenarios:
		</p>
		<ul>
			<li>
				<strong>Parque Cultural Nocturno:</strong> Se realizará en el Parque de los Pies Descalzos durante cinco noches. Allí se programarán <strong>5 artistas locales</strong> (uno por cada noche).
			</li>
			<li>
				<strong>Festival Nacional de la Trova (Ciudad de Medellin):</strong> Se realizarán dos semifinales en la Plaza Gardel, del aeropuerto Olaya Herrera, y la gran final en el Parque de los Pies Descalzos. Para estas tres jornadas se programarán en total <strong>seis artístas locales</strong>, dos por cada día.
			</li>
			<li>
				<strong>Zona que Suena:</strong> Se realizará en Carabobo Norte, entre Parque Explora y el Jardín Botánico. Contará con la participación de seis agrupaciones musicales, dos elencos de danza, seis propuestas artísticas entre clown, malabarismo, magia, circo y cuenteros; para un total de <strong>18 artistas locales programados</strong>.
			</li>
			<li>
				<strong>Escenarios artísticos culturales:</strong> Son cinco escenarios ubicados en los corregimientos de San Cristóbal, Altavista, San Sebastíán de Palmitas, Santa Elena y San Antonio de Prado. En cada uno se programarán:  comparsas,  teatro, danza y música de diferentes géneros. En total, serán <strong>30 artistas locales programados</strong> en éstos escenarios.
			</li>
		</ul>
		
