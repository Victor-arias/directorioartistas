			</p>
			<h4>Condiciones de Participación</h4>

			<p>
				Para el proceso de inscripción y selección de los grupos y artistas, es importante conocer la siguiente información:
			</p>
			<ul>
				<li>
					Esta inscripción es <strong>exclusiva</strong> para agrupaciones, artistas y organizaciones culturales que tengan sede o desarrollen sus actividades en uno de los municipios del <strong>Área Metropolitana del Valle de Aburrá</strong> Propuestas de otras ciudades o países no serán tenidas en cuenta.
				</li>
				<li>
					Las inscripciones se abrirán el <strong>27 de mayo a las 5 pm y se cerrarán el 10 de junio de 2014 a las 6:00 p.m.</strong> hora legal colombiana <a href="http://horalegal.sic.gov.co" taget="_blank" title="Ver la hora legal Colombiana">(UTC -5)</a>. Solo se recibirán propuestas por este medio.
				</li>
				<li>
					En caso de dudas o inquietudes también puede escribir al correo <a href="mailto:juan.martinez@telemedellin.tv" target="_blank">juan.martinez@telemedellin.tv</a>
				</li>
				<li>
					Las propuestas inscritas serán evaluadas por un jurado, conformado por cinco personas de comprobada trayectoria e idoneidad en las artes y la organización de espectáculos, quienes seleccionarán las que mejor cumplan con los criterios de <strong>calidad interpretativa, creatividad, trayectoria y viabilidad técnica y económica</strong>.
				</li>
				<li>
					El resultado con las propuestas escogidas para la programación, se publicará el día 
					<strong>7 de julio de 2014</strong>, en la página web de la <a href="http://www.medellin.gov.co" taget="_blank" title="Ir al portal de la alcaldía de Medellín">Alcaldía de Medellín</a> y en el portal de Feria de las Flores.
				</li>
				<li>
					En todo momento, usted podrá saber cuáles y cuántas propuestas están inscritas, consultando el catálogo en línea del Directorio Artístico de Medellín, al que podrá acceder desde el ícono que encontrará al final de esta página.
				</li>
				<li>
					La organización de la Feria de la Flores 2014 aclara que <strong>diligenciar la inscripción no implica que la propuesta haya sido aceptada para participar en la programación</strong>. Todas deben pasar por el proceso de selección del jurado.
				</li>
				<li>
					Al realizar la inscripción, los participantes dan a entender que aceptan y comprenden las condiciones de este proceso de selección.
				</li>
			</ul>

			<h4>Escenarios de programación de la Feria de las Flores 2014</h4>

			<p>
				Las agrupaciones seleccionadas serán programadas en los siguientes escenarios:
			</p>
			<ul>
				<li>
					<strong>Parque Cultural Nocturno:</strong> Se realizará en el Parque de los Pies Descalzos durante cinco noches. Allí se programarán <strong>5 artistas locales</strong> (uno por cada noche).
				</li>
				<li>
					<strong>Festival Nacional de la Trova (Ciudad de Medellin):</strong> Se realizarán dos semifinales en la Plaza Gardel, del aeropuerto Olaya Herrera, y la gran final en el Parque de los Pies Descalzos. Para estas tres jornadas se programarán en total <strong>seis artístas locales</strong>, dos por cada día.
				</li>
				<li>
					<strong>Zona que Suena:</strong> Se realizará en Carabobo Norte, entre Parque Explora y el Jardín Botánico. Contará con la participación de seis agrupaciones musicales, dos elencos de danza, seis propuestas artísticas entre clown, malabarismo, magia, circo y cuenteros; para un total de <strong>18 artistas o agrupaciones locales programados.</strong>.
				</li>
				<li>
					<strong>Escenarios artísticos culturales:</strong> Son cinco escenarios ubicados en los corregimientos de San Cristóbal, Altavista, San Sebastián de Palmitas, Santa Elena y San Antonio de Prado. En cada uno se programarán:  comparsas,  teatro, danza y música de diferentes géneros. En total, serán <strong>30 artistas locales programados</strong> en éstos escenarios.
				</li>
			</ul>

			<h4>Aspectos Importantes</h4>
			<p>
				Los interesados podrán acceder a una inscripción en internet sin necesidad de imprimir soportes en papel o quemar CDs. Esto, además de hacer un pequeño aporte al cuidado del planeta, debe significar ahorro de tiempo y dinero para los participantes, con la comodidad de no tener que desplazarse ni hacer filas para cumplir con la entrega.
			</p>
			<p>
				La organización de la Feria de las Flores 2014 quiere invitar a los artistas a realizar este trámite con tiempo suficiente y a no dejarlo para último momento. El llamado es también a que todo el sector cultural se apropie de la plataforma y participe activamente con su inscripción.
			</p>
