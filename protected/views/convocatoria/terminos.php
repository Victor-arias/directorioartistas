			</li>
			<li>
				Las inscripciones se abrirán el <strong>27 de mayo y se cerrarán el 13 de junio de 2014 a las 6:00 p.m.</strong> hora legal colombiana <a href="http://horalegal.sic.gov.co" taget="_blank" title="Ver la hora legal Colombiana">(UTC -5)</a>. Solo se recibirán propuestas por este medio.
